cp 测试用例-项目管理模块.csv "$REPORT_DIR/"
cp 测试用例-详细版.xlsx.md "$REPORT_DIR/"
cp 测试数据-*.csv "$REPORT_DIR/" 2>/dev/null || true

# 5. 创建测试报告
echo "5. 创建测试报告..."
cat > "$REPORT_DIR/测试执行报告.md" << 'REPORTEOF'
