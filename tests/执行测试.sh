#!/bin/bash
# 测试执行脚本

echo "========== 项目管理系统测试执行 =========="
echo "开始时间: $(date)"
echo ""

# 1. 环境检查
echo "1. 检查测试环境..."
if command -v python3 &> /dev/null; then
    echo "  ✓ Python3 已安装"
else
    echo "  ✗ Python3 未安装，请先安装Python3"
    exit 1
fi

if command -v pip3 &> /dev/null; then
    echo "  ✓ pip3 已安装"
    # 安装依赖
    pip3 install faker &> /dev/null && echo "  ✓ faker 库已安装"
else
    echo "  ✗ pip3 未安装"
fi

echo ""

# 2. 生成测试数据
echo "2. 生成测试数据..."
python3 生成测试数据.py
echo ""

